const TRANSLATIONS = { ru, tj }
export const LangContext = createContext({ lang: 'ru', t: ru, setLang: () => {} })

export function LangProvider({ children }) {
  const [lang, setLangState] = useState(() => localStorage.getItem('eastcode-lang') || 'ru')
  function setLang(l) { setLangState(l); localStorage.setItem('eastcode-lang', l) }
  const t = TRANSLATIONS[lang] || ru
  return <LangContext.Provider value={{ lang, t, setLang }}>{children}</LangContext.Provider>
}

export function useLang() { return useContext(LangContext) }
